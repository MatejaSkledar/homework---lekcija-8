mood = input("Kako se počutiš (veselo, žalostno, nervozno, sproščeno... )? ")

if mood == "veselo":
    print("Tvoje počutje je sprejemljivo!")

elif mood == "žalostno":
    print("Life goes on!")

elif mood == "nervozno":
    print("Vse bo dobro, verjetno!")

elif mood == "sproščeno":
    print("Veš da maš ti to!")

else:
    print("Tvoje počutje je sumljivo. Obišči zdravnika!")